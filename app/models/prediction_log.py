"""Prediction log model. Additive audit trail of every churn prediction
generated by the deployed system. Does NOT alter existing tables."""
from datetime import datetime
from app.extensions import db


class PredictionLog(db.Model):
    __tablename__ = "prediction_log"

    id = db.Column(db.Integer, primary_key=True, autoincrement=True)
    # customer_id is nullable: ad-hoc predictions from the form may not map to a saved customer.
    customer_id = db.Column(
        db.Integer,
        db.ForeignKey("customers.customer_id", ondelete="SET NULL"),
        nullable=True,
        index=True,
    )
    churn_probability = db.Column(db.Float, nullable=False)
    prediction = db.Column(db.Integer, nullable=False, default=0)
    prediction_label = db.Column(db.String(40), nullable=True)
    risk_level = db.Column(db.String(20), nullable=True)
    health_score = db.Column(db.Integer, nullable=True)
    source = db.Column(db.String(30), nullable=False, default="single", index=True)
    created_at = db.Column(db.DateTime, nullable=False, default=datetime.utcnow, index=True)

    def to_dict(self):
        return {
            "id": self.id,
            "customer_id": self.customer_id,
            "churn_probability": self.churn_probability,
            "prediction": self.prediction,
            "prediction_label": self.prediction_label,
            "risk_level": self.risk_level,
            "health_score": self.health_score,
            "source": self.source,
            "created_at": self.created_at.isoformat() if self.created_at else None,
        }

    def __repr__(self):
        return f"<PredictionLog #{self.id} cust={self.customer_id} prob={self.churn_probability}>"
